package cf.rodolfo.Introduction;

public class Class06_RepetitionStructures03 {
	public static void main(String[] args) {
		// Print the first 25 numbers from a given number;
		int maxValue = 50;
		for (int i = 0; i <= maxValue; i++) {
			if (i > 25) {
				break;
			}
			System.out.print(i + " ");

		}
	}

}
